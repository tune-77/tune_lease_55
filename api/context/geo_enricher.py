def get_region_from_prefecture(prefecture: str) -> str:
    """都道府県名から地域経済圏を返す。"""
    if not prefecture:
        return "不明"
    for pref_key, region in _PREF_TO_REGION.items():
        if pref_key in prefecture:
            return region
    return "不明"


def get_geo_context(prefecture: str = "", industry: str = "") -> str:
    """
    都道府県 or 業種から地域経済コンテキストコメントを生成する。

    Args:
        prefecture: 都道府県名（例: "東京都", "大阪府"）
        industry: 業種名（例: "製造業", "飲食業"）

    Returns:
        地域経済圏コンテキスト文字列
    """
    lines: list[str] = []

    region = get_region_from_prefecture(prefecture)
    if region != "不明":
        lines.append(f"【地域経済圏: {region}】{_REGION_CHARACTERISTICS.get(region, '')}")

    for ind_key, hint in _INDUSTRY_REGION_HINT.items():
        if ind_key in (industry or ""):
            lines.append(f"【業種×地域ヒント】{hint}")
            break

    if not lines:
        return "【地域コンテキスト】都道府県情報なし。全国標準で審査。"

    return "\n".join(lines)
