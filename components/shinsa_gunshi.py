    if resale != "高":
        offers.append({
            "title": "📊 中古業者の査定書取得（物件流動性を客観証明）",
            "detail": "中古業者の査定書を提出し、リセール価値を客観数値で証明。主観ではなく市場価格の『客観的証拠』を突き付けることで担保懸念を封じます。",
            "prob_gain": 0.10,
            "bn_key": "Asset_Liquidity",
        })

    # ── 物件・取引条件（BN: Shorter_Lease_Term） ────────────────────────
    if posterior < 0.80:
        gain = min(0.15, (0.70 - posterior) * 0.8 + 0.05)
