# ベイズ推論エンジン
# ==============================================================================

EVIDENCE_WEIGHTS = {
    "resale_high":   0.15,  # リセール高
    "resale_mid":    0.05,  # リセール中
    "resale_low":   -0.05,  # リセール低
    "repeat":        0.04,  # リピート1件あたり（最大+0.20）
    "subsidy":       0.12,  # 補助金採択
    "bank":          0.10,  # メイン銀行支援
    "intuition":     0.03,  # 直感スライダー1ポイントあたり（基準=3）
}

LEARNING_RATE = 0.08  # 逐次学習の速さ


def compute_prior(score: float, pd_pct: float) -> float:
    """スコアと PD から事前確率を算出。"""
    return max(0.01, min(0.99, (score / 100.0) * (1.0 - pd_pct / 100.0)))


def compute_posterior(
    prior: float,
    resale: str,
    repeat_cnt: int,
    subsidy: bool,
    bank: bool,
    intuition: int,
    success_ratio: float = 0.0,
    similar_case_count: int = 0,
) -> float:
    """証拠を統合して事後確率を更新する。"""
    p = prior

    # 中古市場リセール
    if resale == "高":
        p += EVIDENCE_WEIGHTS["resale_high"]
    elif resale == "中":
        p += EVIDENCE_WEIGHTS["resale_mid"]
    else:
        p += EVIDENCE_WEIGHTS["resale_low"]

    # リピート実績（上限+0.20）
    p += min(EVIDENCE_WEIGHTS["repeat"] * repeat_cnt, 0.20)

    # 補助金採択
    if subsidy:
        p += EVIDENCE_WEIGHTS["subsidy"]

    # メイン銀行支援
    if bank:
        p += EVIDENCE_WEIGHTS["bank"]

    # 担当者直感（基準3、1pt=+0.03/-0.03）
    p += EVIDENCE_WEIGHTS["intuition"] * (intuition - 3)

    # 逐次学習：成功比率による強化
    # P(H|E)_new = P(H|E)_old × (1 + LR × SuccessRatio)
    if success_ratio > 0:
        p = p * (1 + LEARNING_RATE * success_ratio)

    # 類似成約案件がある場合さらに +10%
    if similar_case_count > 0:
        p += 0.10

    return max(0.01, min(0.99, p))


# ==============================================================================
# フレーズ選択ロジック
# ==============================================================================

def select_top_phrases(
    industry_cat: str,
    score: float,
    pd_pct: float,
    resale: str,
    repeat_cnt: int,
    subsidy: bool,
    bank: bool,
    posterior: float,
    asset_name: str = "",
    n: int = 3,
    bn_evidence: dict | None = None,
) -> list[dict]:
    """状況に合致する上位 n フレーズを返す。"""
    vehicle_boost_val, _ = _vehicle_boost_from_asset_name(asset_name)

    # 物件名から「車両系かどうか」を広めに判定（車種名・汎用ワード両対応）
    _asset_up = (asset_name or "").upper()
    _VEHICLE_KEYWORDS = [
        "車", "自動車", "乗用", "トラック", "バン", "ハイエース", "キャラバン",
        "エブリイ", "ハイゼット", "プリウス", "ヤリス", "ノート", "フィット",
        "アクア", "カローラ", "アルファード", "ヴェルファイア", "ステップワゴン",
        "フリード", "セレナ", "ヴォクシー", "ノア", "シエンタ", "NV100",
        "バネット", "アトレー", "NV200", "軽自動車", "普通車", "営業車",
        "ダンプ", "ウイング", "冷凍車", "保冷車",
        # 役員車・高級輸入車も「車両系」として認識
        "役員車", "レクサス", "LEXUS", "外車", "輸入車",
        "営業用トラック", "自家用トラック",
    ]
    is_vehicle = any(kw.upper() in _asset_up for kw in _VEHICLE_KEYWORDS)

    # 役員車・高級外車の検出
    is_exec_car = any(k.upper() in _asset_up for k in _EXEC_CAR_KEYWORDS)

    # 業種×車両フレーズ（非運送業で車両が検出された場合に最優先）
    iv_phrases: list[dict] = []
    if is_vehicle and industry_cat != "運送業":
        iv_phrases = (
            _INDUSTRY_VEHICLE_PHRASES.get(industry_cat)
            or _INDUSTRY_VEHICLE_PHRASES.get("汎用", [])
        )

    # 候補フレーズの組み立て
    # ・運送業 + 車両  → 運送業フレーズ + 商用車フレーズ + 逆転
    # ・運送業以外 + 車両 → 業種×車両フレーズ（最優先）+ 商用車フレーズ + 業種フレーズ + 逆転
    #                        ※ 運送業フレーズは一切混入させない
    # ・車両なし        → 業種フレーズ + 逆転
    if (vehicle_boost_val > 0 or is_vehicle) and industry_cat != "運送業":
        # 非運送業の車両案件
        industry_phrases = PHRASES_100.get(industry_cat, PHRASES_100.get("汎用", []))
        candidates = (
            iv_phrases                       # 業種×車両（最優先）
            + PHRASES_100.get("商用車", [])  # 車両リセール価値
            + industry_phrases               # 業種固有の強み
            + PHRASES_100["逆転アドバイス"]
        )
    elif vehicle_boost_val > 0:
        # 運送業 + 車両：両方混ぜてよい
        candidates = (
            PHRASES_100.get("商用車", [])
            + PHRASES_100.get("運送業", [])
            + PHRASES_100["逆転アドバイス"]
        )
    else:
        # 車両なし：業種フレーズのみ（fallback: 汎用）
        industry_phrases = PHRASES_100.get(industry_cat, PHRASES_100.get("汎用", []))
        candidates = industry_phrases + PHRASES_100["逆転アドバイス"]

    # 物件名のキーワード（大文字化して比較）
    asset_upper = (asset_name or "").upper()
    is_hiace    = any(k.upper() in asset_upper for k in ["ハイエース", "キャラバン"])
    is_keiban   = any(k.upper() in asset_upper for k in ["エブリイ", "エブリィ", "ハイゼット", "NV100", "バネット", "アトレー"])
    is_compact  = any(k.upper() in asset_upper for k in ["ヤリス", "ノート", "フィット", "アクア", "カローラ", "ヴィッツ"])
    is_ct_mri   = any(k.upper() in asset_upper for k in ["CT", "MRI", "PET", "レントゲン", "X線"])
    is_cnc      = any(k.upper() in asset_upper for k in ["マシニング", "CNC", "旋盤", "プレス", "ロボット", "レーザー"])
    is_truck    = any(k.upper() in asset_upper for k in ["トラック", "ダンプ", "ウイング", "冷凍", "保冷"])

    def score_phrase(p: dict) -> float:
        s = p["prob_boost"]
        tags = p.get("tags", [])
        tag_str = " ".join(tags)

        # ── 状況マッチ：補助金 ──
        if subsidy and any(t in tag_str for t in ["補助金", "公的"]):
            s += 0.06
        # ── 状況マッチ：銀行支援・協調リース ──
        if bank and any(t in tag_str for t in ["銀行", "メイン", "協調"]):
            s += 0.05
        # ── 状況マッチ：リピート実績 ──
        if repeat_cnt >= 3 and any(t in tag_str for t in ["リピート", "実績", "信頼"]):
            s += 0.04
        if repeat_cnt >= 5 and any(t in tag_str for t in ["リピート", "実績"]):
            s += 0.03  # さらに加算
        # ── 状況マッチ：リセール高 ──
        if resale == "高" and any(t in tag_str for t in ["中古", "リセール", "担保", "LGD", "残価", "流動"]):
            s += 0.05
        if resale == "低" and any(t in tag_str for t in ["期間短縮", "EL圧縮", "協調", "前受", "メイン行"]):
            s += 0.04
        # ── 状況マッチ：PD高い（要審議・否決圏）→ 逆転・数値証明系を優先 ──
        if pd_pct > 10 and any(t in tag_str for t in ["期間短縮", "保証金", "DSCR", "EL", "査定"]):
            s += 0.06
        # ── 承認確率ゾーン別 ──
        if posterior < 0.60:
            # 危機的 → 逆転アドバイスを最優先
            if p.get("category") == "逆転アドバイス":
                s += 0.08
        elif posterior < 0.70:
            # 要審議 → 逆転 + 数値証明
            if p.get("category") == "逆転アドバイス":
                s += 0.05
            if any(t in tag_str for t in ["DSCR", "試算", "数値", "証拠"]):
                s += 0.03
        elif posterior >= 0.80:
            # 承認圏内 → 業種強みを全面に
            if p.get("category") not in ["逆転アドバイス"]:
                s += 0.04
        # ── 物件種別マッチ：ハイエース系 ──
        if is_hiace and any(t in tag_str for t in ["ハイエース", "LGD", "担保カバー", "輸出"]):
            s += 0.08
        # ── 物件種別マッチ：軽バン系 ──
        if is_keiban and any(t in tag_str for t in ["軽バン", "即日買取", "エブリイ"]):
            s += 0.07
        # ── 物件種別マッチ：コンパクトカー系 ──
        if is_compact and any(t in tag_str for t in ["SUV", "営業車", "コンパクト", "流動性"]):
            s += 0.05
        # ── 物件種別マッチ：CT/MRI等医療機器 ──
        if is_ct_mri and any(t in tag_str for t in ["CT", "MRI", "超音波", "内視鏡", "X線", "中古価値"]):
            s += 0.07
        # ── 物件種別マッチ：工作機械系 ──
        if is_cnc and any(t in tag_str for t in ["CNC", "工作機械", "マシニング", "レーザー加工", "中古価値"]):
            s += 0.07
        # ── 物件種別マッチ：トラック系 ──
        if is_truck and any(t in tag_str for t in ["冷凍車", "小型トラック", "稼働率"]):
            s += 0.06
        # ── 商用車フレーズ全体優先（vehicle_boost_val に比例）──
        if vehicle_boost_val > 0 and p.get("category") == "商用車":
            s += vehicle_boost_val * 0.5
        # ── 業種×車両フレーズを最優先（車両検出 + 非運送業の場合に強力にブースト）──
        if is_vehicle and industry_cat != "運送業" and "業種×車両" in (p.get("category") or ""):
            s += 0.20  # 業種固有の車両フレーズを必ずTOPに持ってくる
        # ── BN逆転シミュレータで選択した条件に対応するフレーズを優先 ──
        if bn_evidence:
            _BN_TAG_BONUS: list[tuple[str, list[str], float]] = [
                ("Asset_Liquidity",    ["中古", "リセール", "担保", "LGD", "残価", "流動"],  0.06),
                ("Shorter_Lease_Term", ["期間短縮", "前受", "セット"],                        0.06),
                ("Core_Business_Use",  ["本業直結", "支払優先"],                              0.06),
                ("Co_Lease",           ["協調", "銀行協調"],                                  0.05),
                ("Parent_Guarantor",   ["親会社", "保証"],                                    0.05),
                ("Related_Assets",     ["個人資産", "保全完結"],                              0.04),
                # Main_Bank_Support: bank=True（フォーム入力）と被る場合は二重加算しない
                ("Main_Bank_Support",  ["メイン", "メイン銀行", "支援"],                      0.04 if not bank else 0.0),
                ("Related_Bank_Status",["信用力", "実績", "銀行取引"],                        0.03),
                ("One_Time_Deal",      ["本件限り", "条件付"],                                0.03),
                ("Insolvent_Status",   ["逆転", "突破", "DSCR", "保証"],                      0.04),
            ]
            for ev_key, bonus_tags, bonus_val in _BN_TAG_BONUS:
                if bonus_val > 0 and bn_evidence.get(ev_key) == 1:
                    if any(bt in tag_str for bt in bonus_tags):
                        s += bonus_val
        return s

    ranked = sorted(candidates, key=score_phrase, reverse=True)

    # 役員車の場合: 警告フレーズを先頭に強制挿入（最終コメントに必ず表示）
    if is_exec_car:
        return _EXEC_CAR_WARNING_PHRASES[:1] + ranked[:n - 1]
    return ranked[:n]


